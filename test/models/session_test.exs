defmodule SessionService.SessionTest do
  use SessionService.ModelCase

  alias SessionService.Session

  @valid_attrs %{uuid: 12345}
  @invalid_attrs %{}

  test "changeset with valid attributes" do
    changeset = Session.changeset(%Session{}, @valid_attrs)
    assert changeset.valid?
  end

  test "changeset with invalid attributes" do
    changeset = Session.changeset(%Session{}, @invalid_attrs)
    refute changeset.valid?
  end
end
